print.mami<-function(x,...){

cat("\n")

if(is.null(x$coefficients)==FALSE){
cat("Estimates for model averaging:\n\n")
print(x$coefficients)
cat("\n")}

if(is.null(x$coefficients.boot)==FALSE){
cat(paste("Bootstrap estimates for model averaging (based on",dim(x$boot.results[[2]])[1],"samples):\n\n",sep=" "))
print(x$coefficients.boot)
cat("\n")}

if(is.null(x$coefficients.s)==FALSE){
cat("Estimates for model selection:\n\n")
print(x$coefficients.s)
cat("\n")}

if(is.null(x$coefficients.boot.s)==FALSE){
cat(paste("Bootstrap estimates for model selection (based on",dim(x$boot.results[[1]])[1],"samples):\n\n",sep=" "))
print(x$coefficients.boot.s)
cat("\n")}

if(is.null(x$variable.importance)==FALSE){
cat("Variable importance (based on model averaging weights):\n\n")
print(x$variable.importance)}

cat("\n")

}